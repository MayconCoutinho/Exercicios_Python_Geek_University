# Leia quatro notas, calcule a média aritmética e imprima o resultado.


nota_1 = float(input('Digite o valor da 1º nota: '))
nota_2 = float(input('Digite o valor da 2º nota: '))
nota_3 = float(input('Digite o valor da 3º nota: '))
nota_4 = float(input('Digite o valor da 4º nota: '))
media = (nota_1+nota_2+nota_3+nota_4)/4
print('Suas notas foram ({0},{1},{2},{3}) e a media é {4}'.format(nota_1,nota_2,nota_3,nota_4,media))

