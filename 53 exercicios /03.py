# Peça ao usuário para digitar três valores inteiros e imprima a soma deles.


n1 = int(input('Digite o valor do 1º número inteiro: '))
n2 = int(input('Digite o valor do 2º número inteiro: '))
n3 = int(input('Digite o valor do 3º número inteiro: '))
soma = n1+n2+n3
print('{0} + {1} + {2} = {3}'.format(n1,n2,n3,soma))